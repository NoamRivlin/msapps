import React from 'react';
import ReactDOM from 'react-dom/client';
import { Provider } from 'react-redux';
import './index.css';
import App from './App';
import reportWebVitals from './reportWebVitals';
import store from './app/store';




const root = ReactDOM.createRoot(document.getElementById('root'));
// we wrap the App component with the Provider component to make the Redux store available to the App component
// we pass the store as a prop to the Provider component
// we wrap the App component with the StrictMode component to check for potential problems
root.render(
  <React.StrictMode>
    <Provider store={store}>
      <App />
    </Provider>
  </React.StrictMode>
);

// If you want to start measuring performance in your app, pass a function
// to log results (for example: reportWebVitals(console.log))
// or send to an analytics endpoint. Learn more: https://bit.ly/CRA-vitals
reportWebVitals();
